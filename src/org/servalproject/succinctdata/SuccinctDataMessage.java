package org.servalproject.succinctdata;

public class SuccinctDataMessage {
	  
	 String prefix = null;
	 String form = null;
	 String timestamp = null;	 
	 String succinctdata = null;
	  
	 public String getPrefix() {
	  return prefix;
	 }
	 public void setPrefix(String prefix) {
	  this.prefix= prefix;
	 }
	 public String getForm() {
	  return form;
	 }
	 public void setForm(String form) {
	  this.form= form;
	 }
	 public String getTimestamp() {
	  return timestamp;
	 }
	 public void setTimestamp(String timestamp) {
	  this.timestamp= timestamp;
	 }
	 public String getSuccinctdata() {
	  return succinctdata;
	 }
	 public void setSuccinctdata(String succinctdata) {
	  this.succinctdata = succinctdata;
	 }
}
